string = "Hello World"
# nchar function
nchar(string)
# toupper function
toupper(string)
# tolower function
tolower(string)
# substr function
substr(string, 5, 20)
# grep function
grep("wor", string)
# paste function
paste("hello", "world", string, sep = "-")
# strsplit function
strsplit(string,'e')
# sprintf function
age = 40L
name = "Ram"
sprintf("%s is %d years old", name, age)
# cat function
cat("hello","world",sep = "-")
# sub function
sub("World", "there!", string)
